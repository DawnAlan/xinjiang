    /**
     * 获取 RestTemplate
     *
     * @param requestUrl 请求路径
     * @return
     */
    private static RestTemplate getRestTemplate(String requestUrl) {
        if (null == restTemplate) {
            restTemplate = new RestTemplate();
            List<HttpMessageConverter<?>> messageConverters = restTemplate.getMessageConverters();
            HttpMessageConverter<?> httpMessageConverter = null;
            // FastJsonHttpMessageConverter fastJsonHttpMessageConverter = new FastJsonHttpMessageConverter();
            for (int i = 0, size = messageConverters.size(); i < size; i++) {
                httpMessageConverter = messageConverters.get(i);
                if (httpMessageConverter instanceof StringHttpMessageConverter) {
                    ((StringHttpMessageConverter) httpMessageConverter).setDefaultCharset(StandardCharsets.UTF_8);
                    messageConverters.set(i, new StringHttpMessageConverter(StandardCharsets.UTF_8));
                }
            }
            // 存在响应时候返回\u9ad8\u901f\u5546\u8d38 Unicode 自动转为正常字符串
            //  restTemplate.getMessageConverters().set(1, fastJsonHttpMessageConverter);
        }
        // https
        if (requestUrl.startsWith("https:")) {
            if (!(restTemplate.getRequestFactory() instanceof HttpsClientRequestFactory)) {
                restTemplate.setRequestFactory(new HttpsClientRequestFactory());
            }
        }
        return restTemplate;
    }

    /**
     * post 请求
     *
     * @param url  请求路径
     * @param data body数据
     * @return
     */
    public static String post(String url, String data) {
        HttpHeaders headers = new HttpHeaders();
        headers.add("Accept", "application/json");
        headers.add("Content-Encoding", "UTF-8");
        headers.add("Content-Type", "application/json; charset=UTF-8");
        HttpEntity<String> requestEntity = new HttpEntity<>(data, headers);
        return getRestTemplate(url).postForObject(url, requestEntity, String.class);
    }

