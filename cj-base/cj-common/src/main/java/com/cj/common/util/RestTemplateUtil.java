    /**
     * 请求提交数据 post请求
     *
     * @param requestUrl 路径
     * @param paramData
     */
    public static String sendForUrlEncoded(String requestUrl, Object paramData) {
        HttpHeaders headers = new HttpHeaders();
        headers.setContentType(MediaType.APPLICATION_FORM_URLENCODED);
        // 数据对象直接转为map
        Map<String, Object> paramMap = convertClass2Map(paramData, false);
        MultiValueMap<String, Object> multiValueMap = new LinkedMultiValueMap<>();
        if (null != paramMap && 0 < paramMap.size()) {
            Set<Map.Entry<String, Object>> entries = paramMap.entrySet();
            for (Map.Entry<String, Object> entry : entries) {
                multiValueMap.add(entry.getKey(), entry.getValue());
            }
        }
        HttpEntity<MultiValueMap<String, Object>> httpEntity = new HttpEntity<>(multiValueMap, headers);
        ResponseEntity<String> stringResponseEntity = getRestTemplate(requestUrl).postForEntity(requestUrl, httpEntity, String.class);
        return (null != stringResponseEntity && null != stringResponseEntity.getBody()) ? stringResponseEntity.getBody() : "";
    }

    public static String sendForUrlEncoded(String requestUrl, MultiValueMap<String, Object> multiValueMap,String token) {
        HttpHeaders headers = new HttpHeaders();
        headers.add("Accept", "*/*");
        headers.add("Content-Encoding", "gzip, deflate");
        headers.add("Content-Type", "application/x-www-form-urlencoded; charset=UTF-8");
        if(StringUtils.isNotEmpty(token)){
            headers.add("Cookie","token="+token);
        }
        // 数据对象直接转为map
        HttpEntity<MultiValueMap<String, Object>> httpEntity = new HttpEntity<>(multiValueMap, headers);
        ResponseEntity<String> stringResponseEntity = getRestTemplate(requestUrl).postForEntity(requestUrl, httpEntity, String.class);
        return (null != stringResponseEntity && null != stringResponseEntity.getBody()) ? stringResponseEntity.getBody() : "";
    }

    /**
     * 将对象反射提取参数名和参数值到map 默认的排序是根据key值进行升序排序
     *
     * @param object        需转换对象
     * @param empty2Default 对应参数值为空的 是否列举 默认保存空值为“”-true 不转换空值-false
     * @return
     */
    private static SortedMap<String, Object> convertClass2Map(Object object, boolean empty2Default) {
        Class clazz = object.getClass();
        SortedMap<String, Object> paramMap = new TreeMap<>();
        try {
            //向上循环  遍历父类
            Field[] field = null;
            for (; clazz != Object.class; clazz = clazz.getSuperclass()) {
                field = clazz.getDeclaredFields();
                for (Field f : field) {
                    f.setAccessible(true);
                    if (null == f.get(object)) {
                        if (empty2Default) {
                            paramMap.put(f.getName(), "");
                        }
                    } else {
                        paramMap.put(f.getName(), f.get(object).toString());
                    }
                }
            }
        } catch (IllegalAccessException e) {
            throw new RuntimeException(e.getMessage(), e);
        }
        return paramMap;
    }

